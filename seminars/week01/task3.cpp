#include <iostream>
#include <minmax.h>

int main()
{
    std::cout<<"The maximum of int data type is: "<<INT_MAX<<std::endl;
    std::cout<<"The minimum of int data type is: "<<INT_MIN<<std::endl;
    std::cout<<"The maximum of unsigned int data type is: "<<UINT_MAX<<std::endl;
    std::cout<<"The maximum of long long data type: "<<LLONG_MAX<<std::endl;
    std::cout<<"The minimum of long long data type: "<<LLONG_MIN<<std::endl;
    std::cout<<"The maximum of unsigned long long data type: "<<ULLONG_MAX<<std::endl;
    std::cout<<"The maximum of char data type: "<<CHAR_MAX<<std::endl;
    std::cout<<"The minimum of char data type: "<<CHAR_MIN<<std::endl;
    std::cout<<"The maximum of signed char data type: "<<SCHAR_MAX<<std::endl;
    std::cout<<"The minimum of signed char data type: "<<SCHAR_MIN<<std::endl;
    std::cout<<"The maximum of unsigned char data type: "<<UCHAR_MAX<<std::endl;
    std::cout<<"The minimum of short data type: "<<SHRT_MIN<<std::endl;
    std::cout<<"The maximum of short data type: "<<SHRT_MAX<<std::endl;
    std::cout<<"The maximum of unsigned short data type: "<<USHRT_MAX<<std::endl;

    return 0;
}

